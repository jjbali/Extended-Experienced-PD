public class MultiItem extends Questionnaire {
    {
        image = ItemSpriteSheet.TYPING;
        unique = true;
        identify();
        defaultAction = AC_ANSWER;
    }

    private static final String AC_ANSWER = "ANSWER";
    private static final String AC_SHOP = "SHOP";

    @Override
    public ArrayList<String> actions(Hero hero ) {
        ArrayList<String> actions = super.actions( hero );
        actions.add( AC_ANSWER );
        actions.remove( AC_THROW );
        actions.remove( AC_DROP );
        return actions;
    }

    @Override
    public boolean isIdentified() {
        return true;
    }

    @Override
    public boolean isUpgradable() {
        return false;
    }

    @Override
    public void execute( Hero hero, String action ) {
        super.execute( hero, action );
        if (action.equals( AC_ANSWER )) {
            Questionnaire.askCodeMulti();
        }
    }


    @Override
    public ItemSprite.Glowing glowing() {
        return new ItemSprite.Glowing( 0.2f );
    }

    @Override
    public String name() {
        return "Optional Questionnaire";
    }

    @Override
    public String desc() {
        return "This item represents your intelligence, choose any of those questionnaires and collect points as soon as possible! There is no penalty such as reducing your points as you get the answers wrong.\n\n"
                + "Points: " + Questionnaire.points_gathered;
    }
}
