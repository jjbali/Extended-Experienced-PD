		itemsToSpawn.add(new TieredCard().upgrade(Random.Int(10, 15) + 1));

		itemsToSpawn.add(new TieredCard().upgrade(Random.Int(16, 20) + 1));
		itemsToSpawn.add(new TieredCard().upgrade(Random.Int(16, 20) + 1));
		itemsToSpawn.add(new TieredCard().upgrade(Random.Int(16, 20) + 1));
		itemsToSpawn.add(new TieredCard().upgrade(Random.Int(16, 20) + 1));

		itemsToSpawn.add(new TieredCard().upgrade(Random.Int(21, 29) + 1));
		itemsToSpawn.add(new TieredCard().upgrade(Random.Int(21, 29) + 1));
		itemsToSpawn.add(new TieredCard().upgrade(Random.Int(21, 29) + 1));
